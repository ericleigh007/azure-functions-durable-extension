    [Function("SignalActivityRecorderEntity")]
    public static async Task<HttpResponseData> SignalActivityRecorderEntity_HttpStart(
        [HttpTrigger(AuthorizationLevel.Anonymous, "get", "post")] HttpRequestData req,
        [DurableClient] DurableTaskClient client)
    {
        var mainEntityId = new EntityInstanceId("ActivityRecorderEntity", "mainEntity");
        await client.Entities.SignalEntityAsync(mainEntityId, "signal_secondary_entity");
        return req.CreateResponse(HttpStatusCode.OK);
    }

    [Function("ActivityRecorderEntity")]
    public static Task DispatchActivityRecorderEntityAsync([EntityTrigger] TaskEntityDispatcher dispatcher)
    {
        return dispatcher.DispatchAsync(operation =>
        {
            if (operation.State.GetState(typeof(List<string?>)) is null)
            {
                operation.State.SetState(new List<string?>());
            }

            List<string?> state = operation.State.GetState<List<string?>>()!;
            state.Add(Activity.Current?.TraceId.ToString());
            operation.State.SetState(state);

            switch (operation.Name.ToLowerInvariant())
            {
                case "get_activity_info":
                    return new(state);
                case "record_activity_info":
                    // Do nothing since the Activity info is already recorded in the state.
                    break;
                case "signal_secondary_entity":
                    var secondaryEntityId = new EntityInstanceId("ActivityRecorderEntity", "secondaryEntity");
                    operation.Context.SignalEntity(secondaryEntityId, "record_activity_info");
                    break;
                case "reset":
                    state.Clear();
                    operation.State.SetState(state);
                    break;
            }

            return default;
        });
    }

    [Function("StartOrchestrationEntity")]
    public static Task DispatchStartOrchestrationEntityAsync([EntityTrigger] TaskEntityDispatcher dispatcher)
    {
        return dispatcher.DispatchAsync(operation =>
        {
            if (operation.State.GetState(typeof(List<string?>)) is null)
            {
                operation.State.SetState(new List<string?>());
            }

            List<string?> state = operation.State.GetState<List<string?>>()!;
            state.Add(Activity.Current?.TraceId.ToString());
            operation.State.SetState(state);

            if (operation.Name.ToLowerInvariant() == "start_orchestration")
            {
                state.Add(operation.Context.ScheduleNewOrchestration("GetMainActivityInfoOrchestration"));
                operation.State.SetState(state);
                return new(state);
            }
            else if (operation.Name.ToLowerInvariant() == "reset")
            {
                state.Clear();
                operation.State.SetState(state);
            }

            return default;
        });
    }
