    [Function(nameof(DistributedTracingEntitiesOrchestration))]
    public static async Task<List<string?>> RunDistributedTracingEntitiesOrchestration(
        [OrchestrationTrigger] TaskOrchestrationContext context)
    {
        var mainEntityId = new EntityInstanceId("ActivityRecorderEntity", "mainEntity");
        var secondaryEntityId = new EntityInstanceId("ActivityRecorderEntity", "secondaryEntity");
        var orchestrationStarterEntityId = new EntityInstanceId("StartOrchestrationEntity", "orchestrationStarter");

        // Reset the state to remove trace IDs from previous runs
        await context.Entities.CallEntityAsync(mainEntityId, "reset");
        await context.Entities.CallEntityAsync(secondaryEntityId, "reset");
        await context.Entities.CallEntityAsync(orchestrationStarterEntityId, "reset");

        var activityAndOrchestrationIds = new List<string?> { Activity.Current?.TraceId.ToString() };

        await context.Entities.SignalEntityAsync(mainEntityId, "signal_secondary_entity");

        activityAndOrchestrationIds.AddRange(await context.Entities.CallEntityAsync<List<string?>>(mainEntityId, "get_activity_info"));
        activityAndOrchestrationIds.AddRange(await context.Entities.CallEntityAsync<List<string?>>(secondaryEntityId, "get_activity_info"));

        activityAndOrchestrationIds.AddRange(await context.Entities.CallEntityAsync<List<string?>>(orchestrationStarterEntityId, "start_orchestration"));
        return activityAndOrchestrationIds;
    }

    [Function("GetMainActivityInfoOrchestration")]
    public static async Task<List<string?>> RunGetMainActivityInfoOrchestration(
        [OrchestrationTrigger] TaskOrchestrationContext context)
    {
        var activityTraceIds = new List<string?> { Activity.Current?.TraceId.ToString() };
        var mainEntityId = new EntityInstanceId("ActivityRecorderEntity", "mainEntity");

        // Reset the state to remove the trace IDs generated by the DistributedTracingEntitiesOrchestration to avoid listing them redundantly
        await context.Entities.CallEntityAsync<List<string?>>(mainEntityId, "reset");
        activityTraceIds.AddRange(await context.Entities.CallEntityAsync<List<string?>>(mainEntityId, "get_activity_info"));
        return activityTraceIds;
    }

