    [Function("GetActivityInfoOrchestration_Output")]
    public static async Task<HttpResponseData> GetOrchestrationOutput(
        [HttpTrigger(AuthorizationLevel.Anonymous, "get", "post")] HttpRequestData req,
        [DurableClient] DurableTaskClient client,
        string id)
    {
        OrchestrationMetadata? metadata = await client.WaitForInstanceCompletionAsync(instanceId: id, getInputsAndOutputs: true);

        HttpResponseData response;
        if (metadata == null)
        {
            response = req.CreateResponse(HttpStatusCode.NotFound);
            await response.WriteStringAsync("Orchestration metadata not found.");
            return response; // Return a 404 response if metadata is null
        }

        var output = metadata.ReadOutputAs<JsonArray>();

        response = req.CreateResponse(HttpStatusCode.OK);
        response.Headers.Add("Content-Type", "application/json");
        await response.WriteStringAsync(output!.ToString());

        return response;
    }

